let calculator = {
    add: function(a, b) {
        return a + b;
    },
    subtract: function(a, b) {
        return a - b;
    },
    multiply: function(a, b) {
        return a * b;
    },
    divide: function(a, b) {
        return a / b;
    }
};

console.log(calculator.add(45, 45));
console.log(calculator.subtract(45, 45));
console.log(calculator.multiply(45, 45));
console.log(calculator.divide(45, 45));